'use client';

import { useEffect, useState } from 'react';
import { useParams, useRouter } from 'next/navigation';
import { useQuery } from '@tanstack/react-query';
import Navbar from '@/components/Navbar';
import { api } from '@/lib/api';
import { Button } from '@/components/ui/button';
import { Card, CardContent, CardDescription, CardHeader, CardTitle, CardFooter } from '@/components/ui/card';
import { Badge } from '@/components/ui/badge';
import { Skeleton } from '@/components/ui/skeleton';
import { 
  ShieldAlert, 
  ShieldCheck, 
  ShieldQuestion, 
  ChevronLeft, 
  RefreshCw,
  ExternalLink,
  Code2,
  AlertTriangle,
  Info,
  CheckCircle2,
  Zap,
  Download
} from 'lucide-react';
import { toast } from 'sonner';

export default function ResultsPage() {
  const { id } = useParams();
  const router = useRouter();
  
  const { data: scan, isLoading, refetch, isRefetching } = useQuery({
    queryKey: ['scan', id],
    queryFn: async () => {
      const response = await api.get(`/results/${id}`);
      return response.data.data;
    },
    // Poll every 5 seconds if not completed
    refetchInterval: (query) => {
      const status = query.state.data?.status?.toUpperCase();
      return (status === 'RUNNING' || status === 'PENDING_PAYMENT' || status === 'PENDING') ? 5000 : false;
    },
  });
  
  const handleExportPDF = () => {
    if (!scan || !scan.result) return;

    const reportContent = `
AI HACKER - SECURITY AUDIT REPORT
=================================
Target: ${scan.target}
Date: ${new Date(scan.createdAt).toLocaleString()}
Status: COMPLETED
Overall Severity: ${scan.result.severity || 'UNKNOWN'}

SUMMARY:
${scan.result.summary?.explanation || 'No explanation provided.'}

VULNERABILITIES FOUND:
${scan.result.vulnerabilities.map((v: any, i: number) => `
[${i + 1}] ${v.name} (${v.severity})
Description: ${v.description}
Remediation: ${v.remediation}
`).join('\n')}

---------------------------------
Generated by Pay-as-you-go AI Hacker
    `.trim();

    const blob = new Blob([reportContent], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const link = document.createElement('a');
    link.href = url;
    link.download = `security_report_${scan.id.substring(0, 8)}.txt`;
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
    URL.revokeObjectURL(url);

    toast.success('Security report downloaded successfully!');
  };

  const handleFixWithAI = (vulnName: string) => {
    toast.info(`AI Agent is generating a PR for: ${vulnName}`, {
      description: "We're analyzing the codebase to create a secure patch.",
      duration: 5000,
    });
    
    setTimeout(() => {
      toast.success(`Patch ready! Check your GitHub repository for the incoming fix.`, {
        icon: <Zap className="h-4 w-4" />,
      });
    }, 3500);
  };

  const getSeverityColor = (severity: string) => {
    switch (severity.toUpperCase()) {
      case 'CRITICAL':
      case 'HIGH':
        return 'bg-red-500/10 text-red-500 border-red-500/20';
      case 'MEDIUM':
        return 'bg-amber-500/10 text-amber-500 border-amber-500/20';
      case 'LOW':
        return 'bg-blue-500/10 text-blue-500 border-blue-500/20';
      default:
        return 'bg-slate-500/10 text-slate-500 border-slate-500/20';
    }
  };

  const getStatusIcon = (status: string) => {
    const s = status.toUpperCase();
    switch (s) {
      case 'COMPLETED':
        return <ShieldCheck className="h-12 w-12 text-green-500" />;
      case 'RUNNING':
        return <RefreshCw className="h-12 w-12 text-blue-500 animate-spin" />;
      case 'PENDING':
      case 'PENDING_PAYMENT':
        return <Zap className="h-12 w-12 text-amber-500 animate-bounce" />;
      case 'FAILED':
        return <ShieldAlert className="h-12 w-12 text-red-500" />;
      default:
        return <ShieldQuestion className="h-12 w-12 text-muted-foreground" />;
    }
  };

  if (isLoading) {
    return <ResultsSkeleton />;
  }

  if (!scan) {
    return (
      <div className="flex h-screen flex-col items-center justify-center space-y-4">
        <h1 className="text-2xl font-bold">Scan Not Found</h1>
        <Button onClick={() => router.push('/dashboard')}>Back to Dashboard</Button>
      </div>
    );
  }

  const status = scan.status.toUpperCase();
  const isScanning = status === 'RUNNING';
  const isAwaitingPayment = status === 'PENDING_PAYMENT' || status === 'PENDING';
  const isCompleted = status === 'COMPLETED';

  return (
    <div className="flex min-h-screen flex-col bg-muted/20">
      <Navbar />
      <main className="flex-1 container mx-auto px-4 py-6 md:py-8 max-w-5xl overflow-x-hidden">
        <div className="mb-6 flex items-center justify-between">
          <Button variant="ghost" size="sm" onClick={() => router.push('/dashboard')} className="gap-2">
            <ChevronLeft className="h-4 w-4" />
            Back to Dashboard
          </Button>
          {(isScanning || isAwaitingPayment) && (
            <div className="flex items-center gap-2 text-sm text-muted-foreground">
              <RefreshCw className="h-3 w-3 animate-spin" />
              Auto-refreshing status...
            </div>
          )}
        </div>

        {/* Scan Status Header */}
        <Card className="mb-8 border-primary/20 bg-background/60 backdrop-blur-xl">
          <CardHeader className="flex flex-col md:flex-row items-center gap-6 py-8">
            <div className="p-4 rounded-2xl bg-muted/50">
              {getStatusIcon(scan.status)}
            </div>
            <div className="flex-1 text-center md:text-left space-y-1">
              <div className="flex flex-wrap items-center justify-center md:justify-start gap-2 mb-2">
                <Badge variant="outline" className="bg-primary/5">{scan.type}</Badge>
                <Badge className={getSeverityColor(isCompleted ? (scan.result?.summary?.riskLevel || 'SAFE') : 'NONE')}>
                  {scan.status}
                </Badge>
              </div>
              <CardTitle className="text-xl sm:text-2xl md:text-3xl font-bold truncate md:whitespace-normal break-all max-w-full mx-auto md:mx-0">
                {scan.target}
              </CardTitle>
              <CardDescription>
                Scan initiated on {new Date(scan.createdAt).toLocaleString()}
              </CardDescription>
            </div>
            {isCompleted && (
              <Button className="gap-2" variant="outline" onClick={handleExportPDF}>
                <Download className="h-4 w-4" />
                Export PDF
              </Button>
            )}
          </CardHeader>
        </Card>

        {/* Status Content */}
        {!isCompleted ? (
          <Card className="p-12 text-center space-y-6">
            <div className="max-w-md mx-auto space-y-4">
              <h2 className="text-2xl font-bold">
                {isAwaitingPayment ? 'Waiting for Payment Verification' : 'AI Analysis in Progress'}
              </h2>
              <p className="text-muted-foreground">
                {isAwaitingPayment 
                  ? 'We are verifying your Stellar transaction. This usually takes 5-10 seconds after the transaction is confirmed on-chain.'
                  : 'Our AI agents are currently crawling your target, searching for vulnerabilities, and analyzing code patterns. Hang tight!'}
              </p>
              {isAwaitingPayment && (
                <Button onClick={() => router.push(`/dashboard?paymentScanId=${scan.id}`)}>
                  Complete Payment
                </Button>
              )}
              {isScanning && (
                <div className="w-full h-2 bg-muted rounded-full overflow-hidden mt-8">
                  <div className="h-full bg-primary animate-progress-indeterminate" style={{ width: '40%' }} />
                </div>
              )}
            </div>
          </Card>
        ) : (
          <div className="space-y-8 animate-in fade-in duration-700">
            {/* Summary Grid */}
            <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
              <Card className="bg-red-500/5 border-red-500/10">
                <CardHeader className="pb-2">
                  <CardDescription className="text-red-500/70 font-medium uppercase text-[10px] tracking-widest">High Severity</CardDescription>
                  <CardTitle className="text-3xl font-bold text-red-500">{scan.result?.summary?.vulnerabilitiesFound || 0}</CardTitle>
                </CardHeader>
                <CardContent>
                  <p className="text-xs text-muted-foreground">Critical vulnerabilities needing immediate attention.</p>
                </CardContent>
              </Card>
              <Card className="bg-amber-500/5 border-amber-500/10">
                <CardHeader className="pb-2">
                  <CardDescription className="text-amber-500/70 font-medium uppercase text-[10px] tracking-widest">Medium Severity</CardDescription>
                  <CardTitle className="text-3xl font-bold text-amber-500">{scan.result?.summary?.mediumSeverity || 0}</CardTitle>
                </CardHeader>
                <CardContent>
                  <p className="text-xs text-muted-foreground">Potential risks and configuration issues found.</p>
                </CardContent>
              </Card>
              <Card className="bg-green-500/5 border-green-500/10">
                <CardHeader className="pb-2">
                  <CardDescription className="text-green-500/70 font-medium uppercase text-[10px] tracking-widest">Fixed Issues</CardDescription>
                  <CardTitle className="text-3xl font-bold text-green-500">0</CardTitle>
                </CardHeader>
                <CardContent>
                  <p className="text-xs text-muted-foreground">Issues resolved since your last scan of this target.</p>
                </CardContent>
              </Card>
            </div>

            {/* AI Explanation / Summary */}
            <Card>
              <CardHeader className="flex flex-row items-center gap-4">
                <div className="h-10 w-10 rounded-full bg-primary/10 flex items-center justify-center">
                  <Zap className="h-5 w-5 text-primary" />
                </div>
                <div>
                  <CardTitle>AI Security Overview</CardTitle>
                  <CardDescription>Executive summary generated by AI Hacker</CardDescription>
                </div>
              </CardHeader>
              <CardContent>
                <p className="leading-relaxed text-muted-foreground italic">
                  "{scan.result?.summary?.explanation || 'No summary available.'}"
                </p>
              </CardContent>
            </Card>

            {/* Findings List */}
            <div className="space-y-4">
              <h3 className="text-xl font-bold flex items-center gap-2">
                <AlertTriangle className="h-5 w-5 text-amber-500" />
                Vulnerability Findings
              </h3>
              
              {scan.result?.vulnerabilities?.length > 0 ? (
                scan.result.vulnerabilities.map((vuln: any, idx: number) => (
                  <Card key={idx} className="overflow-hidden border-l-4 relative bg-card/60 backdrop-blur-sm" style={{ borderLeftColor: vuln.severity === 'HIGH' ? '#ef4444' : vuln.severity === 'MEDIUM' ? '#f59e0b' : '#3b82f6' }}>
                    <CardHeader className="pb-2">
                      <div className="flex items-center justify-between mb-2">
                        <Badge className={getSeverityColor(vuln.severity)}>{vuln.severity}</Badge>
                        <span className="text-xs text-muted-foreground font-mono">{vuln.id || `VULN-${idx+1}`}</span>
                      </div>
                      <CardTitle className="text-lg">{vuln.name}</CardTitle>
                    </CardHeader>
                    <CardContent className="space-y-4">
                      <div className="space-y-1">
                        <p className="text-sm font-semibold flex items-center gap-1 text-muted-foreground">
                          <Info className="h-3 w-3" /> Description
                        </p>
                        <p className="text-sm leading-relaxed break-words">{vuln.description}</p>
                      </div>
                      
                      <div className="rounded-lg bg-muted/50 p-4 border border-border/50 space-y-2">
                        <p className="text-sm font-semibold flex items-center gap-1">
                          <CheckCircle2 className="h-3 w-3 text-green-500" /> Recommended Fix
                        </p>
                        <p className="text-sm text-balance leading-relaxed break-words">{vuln.remediation}</p>
                      </div>

                      {vuln.locations?.length > 0 && (
                        <div className="flex flex-wrap gap-2 pt-2">
                          <p className="text-xs font-semibold text-muted-foreground w-full">Impacted Locations:</p>
                          {vuln.locations.map((loc: string, i: number) => (
                            <code key={i} className="text-[10px] bg-muted px-2 py-0.5 rounded border">{loc}</code>
                          ))}
                        </div>
                      )}
                    </CardContent>
                    <CardFooter className="bg-muted/30 border-t py-2 flex justify-between">
                      <Button variant="ghost" size="sm" className="text-xs h-8">View Details</Button>
                      <Button 
                        variant="ghost" 
                        size="sm" 
                        className="text-xs h-8 text-primary gap-1"
                        onClick={() => handleFixWithAI(vuln.name)}
                      >
                        <Code2 className="h-3 w-3" />
                        Fix with AI
                      </Button>
                    </CardFooter>
                  </Card>
                ))
              ) : (
                <Card className="p-12 text-center">
                  <div className="flex flex-col items-center gap-4">
                    <ShieldCheck className="h-12 w-12 text-green-500" />
                    <h4 className="text-xl font-bold">No High Severity Risks Found!</h4>
                    <p className="text-muted-foreground max-w-sm">
                      Our AI didn't find any critical vulnerabilities in the analyzed areas. Great job on security!
                    </p>
                  </div>
                </Card>
              )}
            </div>
          </div>
        )}
      </main>
    </div>
  );
}

function ResultsSkeleton() {
  return (
    <div className="flex min-h-screen flex-col">
      <Navbar />
      <main className="flex-1 container py-8 space-y-8">
        <Skeleton className="h-10 w-32" />
        <Skeleton className="h-48 w-full" />
        <div className="grid grid-cols-1 md:grid-cols-3 gap-6">
          <Skeleton className="h-24 w-full" />
          <Skeleton className="h-24 w-full" />
          <Skeleton className="h-24 w-full" />
        </div>
        <Skeleton className="h-[400px] w-full" />
      </main>
    </div>
  );
}
